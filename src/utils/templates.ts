export const createNodeFromTemplate = (template: Template, position: { x: number; y: number }) => {
  return template.nodes.map(node => ({
    ...node,
    id: uuidv4(),
    position: {
      x: node.position.x + position.x - 400,
      y: node.position.y + position.y - 300,
    },
  }));
};